-- 7. RPC FUNCTIONS
-- This function allows admins to securely delete a user from the client app.
CREATE OR REPLACE FUNCTION delete_user(target_user_id UUID)
RETURNS void AS $$
BEGIN
  -- Check if the current user has admin privileges
  IF NOT EXISTS (
    SELECT 1 FROM profiles 
    WHERE id = auth.uid() AND role IN ('admin', 'superuser')
  ) THEN
    RAISE EXCEPTION 'Unauthorized: Only admins can delete users.';
  END IF;

  -- Delete related records first to avoid foreign key violations
  DELETE FROM appointments WHERE patient_id = target_user_id OR doctor_id = target_user_id;
  DELETE FROM notifications WHERE user_id = target_user_id OR sender_id = target_user_id;
  
  -- Delete the user from auth.users (this will cascade to profiles and doctors)
  DELETE FROM auth.users WHERE id = target_user_id;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;
